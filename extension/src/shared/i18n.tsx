export function t(key: string, locale: UILocale): string {
  const entry = dict[key];
  if (!entry) return key;
  return entry[locale] ?? entry.en;
}

export function useT(settings: UserSettings) {
  const locale = settings.uiLocale ?? 'en';
  return useCallback((key: string) => t(key, locale), [locale]);
}

export function LocaleToggle({ settings, onToggle, style }: {
  settings: UserSettings;
  onToggle: (locale: UILocale) => void;
  style?: React.CSSProperties;
}) {
  const current = settings.uiLocale ?? 'en';
  const next = current === 'en' ? 'tr' : 'en';
  return (
    <button
      onClick={() => onToggle(next)}
      title={current === 'en' ? 'Türkçeye geç' : 'Switch to English'}
      style={{
        width: '36px',
        height: '36px',
        borderRadius: '8px',
        border: '2px solid #98C1D9',
        background: current === 'tr' ? '#98C1D9' : 'transparent',
        color: current === 'tr' ? '#FFFFFF' : '#98C1D9',
        fontSize: '12px',
        fontWeight: 800,
        cursor: 'pointer',
        display: 'flex',
        alignItems: 'center',
        justifyContent: 'center',
        transition: 'all 0.15s',
        flexShrink: 0,
        ...style,
      }}
    >
      {current === 'en' ? 'TR' : 'EN'}
    </button>
  );
}
